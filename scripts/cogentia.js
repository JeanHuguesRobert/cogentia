// Continuation-based embeddings indexing
// Instead of calling OpenAI directly, emit a continuation for external agent
async function indexEmbeddings(ctx, options = {}) {
  const opened = await openIndexDatabase({ create: false });
  if (!opened.ok) return { ok: false, error: opened.code };
  const { db } = opened;

  try {
    await ensureEmbeddingsTable(db);

    const view = normalizeDaemonView(options.view || "") === FULL_VIEW ? FULL_VIEW : PUBLIC_VIEW;
    const repoFilter = String(options.repo || "all");
    const limit = boundedInteger(options.limit, 1000, 1, 5000);
    const force = Boolean(options.force);
    const embeddingProfile = resolveEmbeddingProfile(ctx, options);
    const targetProvider = embeddingProfile.provider && embeddingProfile.provider !== "unknown" ? embeddingProfile.provider : null;

    const repoClause = repoFilter === "all" ? "" : "AND c.repo = ?";
    const publicClause = view === FULL_VIEW ? "" : "AND c.searchable_public = 1";

    const chunksToEmbed = db.prepare(`
      SELECT c.id, c.repo, c.path, c.start_line, c.end_line, c.text, c.content_hash
      FROM chunks c
      WHERE c.text IS NOT NULL AND c.text != ''
        AND c.text != 'null'
        ${publicClause}
        ${repoClause}
      ORDER BY c.repo, c.path, c.start_line
      LIMIT ?
    `).all(...(repoFilter !== "all" ? [repoFilter, limit] : [limit]));

    // Filter out chunks that already have embeddings from the target provider
    // Deduplication is per-provider: multiple providers can embed the same chunk
    // The UNIQUE(chunk_id, provider, model_name) constraint prevents true duplicates
    let existingChunkIds = new Set();
    if (targetProvider) {
      // Only exclude chunks already embedded by this specific provider
      existingChunkIds = new Set(
        db.prepare("SELECT chunk_id FROM embeddings WHERE provider = ?").all(targetProvider).map(r => r.chunk_id)
      );
    }
    // When targetProvider is null, don't exclude any chunks - let each provider embed independently

    const toProcess = chunksToEmbed.filter(c => force || !existingChunkIds.has(c.id));

    db.close();

    // Emit a continuation for the external agent
    const chunksData = toProcess.map(c => ({
      chunk_id: c.id,
      content_hash: c.content_hash,
      repo: c.repo,
      path: c.path,
      start_line: c.start_line,
      end_line: c.end_line,
      text: c.text,
    }));

    return emitContinuation(ctx, {
      kind: "embeddings-index",
      title: `Generate embeddings for ${chunksData.length} chunks`,
      question: `Generate vector embeddings for ${chunksData.length} text chunks using ${describeEmbeddingProfile(embeddingProfile)}. ${describeEmbeddingCredentials(embeddingProfile)} Return embeddings as JSON array.`,
      priority: 1,
      dedupe_key: `embeddings-${repoFilter}-${view}-${limit}-${embeddingProfile.provider}-${embeddingProfile.model_name}`,
      subject: { repo: repoFilter, path: "" },
      context: {
        chunks: chunksData,
        view,
        repo: repoFilter,
        limit,
        force,
        embedding_profile: embeddingProfile,
      },
      expected_response: {
        format: "json",
        required: ["embeddings"],
        schema: {
          embeddings: [
            {
              chunk_id: "integer",
              content_hash: "string",
              embedding: "number[]",
              provider: "string",
              model_name: "string",
              dimensions: "integer",
            },
          ],
        },
      },
    });
  } catch (e) {
    db?.close();
    return { ok: false, error: "embeddings_failed", message: e.message };
  }
}

// Store embeddings provided by external agent (via continuation resolution)
async function storeEmbeddings(ctx, embeddings) {
  const opened = await openIndexDatabase({ create: false });
  if (!opened.ok) return { ok: false, error: opened.code };
  const { db } = opened;

  try {
    await ensureEmbeddingsTable(db);

    let stored = 0;
    for (const item of embeddings) {
      const chunk = db.prepare("SELECT repo, path, start_line, end_line FROM chunks WHERE id = ?").get(item.chunk_id);
      if (!chunk) continue;

      const stmt = db.prepare(`
        INSERT OR REPLACE INTO embeddings
        (chunk_id, content_hash, provider, repo, path, start_line, end_line, embedding, model_name, dimensions, created_at)
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, datetime('now'))
      `);

      // Infer provider from model_name if not specified
      let provider = item.provider || "unknown";
      if (!item.provider) {
        if (item.model_name?.includes("text-embedding")) {
          provider = "openai";
        } else if (item.model_name?.includes("mxbai")) {
          provider = "magistral";
        }
      }

      stmt.run(
        item.chunk_id,
        item.content_hash,
        provider,
        chunk.repo,
        chunk.path,
        chunk.start_line,
        chunk.end_line,
        JSON.stringify(item.embedding),
        item.model_name || "unknown",
        item.dimensions || item.embedding?.length || 0
      );
      stored++;
    }

    db.close();
    return { ok: true, stored };
  } catch (e) {
    db?.close();
    return { ok: false, error: "store_failed", message: e.message };
  }
}

// Semantic search using cosine similarity on stored embeddings
async function semanticSearch(ctx, query, options = {}) {
  const q = normalizeContextQuery(query);
  const view = normalizeDaemonView(options.view) === FULL_VIEW ? FULL_VIEW : PUBLIC_VIEW;
  const repo = String(options.repo || "all");
  const limit = boundedInteger(options.limit, 10, 1, 50);
  const embeddingProfile = resolveEmbeddingProfile(ctx, options);

  const opened = await openIndexDatabase({ create: false });
  if (!opened.ok) return { ok: false, error: opened.code, query: q, mode: "semantic" };
  const { db } = opened;

  try {
    const hasEmbeddings = db.prepare("SELECT count(*) as count FROM embeddings").get();
    if (!hasEmbeddings || hasEmbeddings.count === 0) {
      db.close();
      return {
        ok: false,
        error: "no_embeddings",
        query: q,
        mode: "semantic",
        message: "No embeddings indexed. Run: cogentia.js embeddings index",
      };
    }

    // Emit a continuation for query embedding
    db.close();
    return emitContinuation(ctx, {
      kind: "semantic-search",
      title: `Semantic search for "${q}"`,
      question: `Generate a vector embedding for the query "${q}" using ${describeEmbeddingProfile(embeddingProfile)} and return it for semantic similarity search against ${hasEmbeddings.count} indexed chunks. ${describeEmbeddingCredentials(embeddingProfile)}`,
      priority: 1,
      dedupe_key: `semantic-${q.slice(0, 50)}-${embeddingProfile.provider}-${embeddingProfile.model_name}`,
      subject: { repo, path: "" },
      context: {
        query: q,
        view,
        repo,
        limit,
        indexed_count: hasEmbeddings.count,
        embedding_profile: embeddingProfile,
      },
      expected_response: {
        format: "json",
        required: ["query_embedding", "model_name", "dimensions"],
        schema: {
          query_embedding: "number[]",
          provider: "string",
          model_name: "string",
          dimensions: "integer",
        },
      },
    });
  } catch (e) {
    db?.close();
    return { ok: false, error: "semantic_search_failed", query: q, mode: "semantic", ...(view === FULL_VIEW ? { message: e.message } : {}) };
  }
}

// Execute semantic search with provided query embedding
async function semanticSearchWithEmbedding(ctx, queryEmbedding, options = {}) {
  const q = normalizeContextQuery(options.query || "");
  const view = normalizeDaemonView(options.view) === FULL_VIEW ? FULL_VIEW : PUBLIC_VIEW;
  const repo = String(options.repo || "all");
  const limit = boundedInteger(options.limit, 10, 1, 50);
  const preferredProvider = options.provider || null;
  const priority = options.priority || "balanced";

  const opened = await openIndexDatabase({ create: false });
  if (!opened.ok) return { ok: false, error: opened.code, query: q, mode: "semantic" };
  const { db } = opened;

  try {
    const repoClause = repo === "all" ? "" : "AND c.repo = ?";
    const publicClause = view === FULL_VIEW ? "" : "AND c.searchable_public = 1";

    // Build provider clause
    let providerClause = "";
    let providerParams = [];
    if (preferredProvider) {
      providerClause = "AND e.provider = ?";
      providerParams = [preferredProvider];
    } else {
      // If no provider specified, prefer the one with the most embeddings
      const providerCounts = db.prepare(`
        SELECT provider, COUNT(*) as count
        FROM embeddings e
        JOIN chunks c ON e.chunk_id = c.id
        WHERE 1=1 ${publicClause}
        GROUP BY provider
        ORDER BY count DESC
        LIMIT 1
      `).get(...(publicClause ? [] : []));

      if (providerCounts) {
        providerClause = "AND e.provider = ?";
        providerParams = [providerCounts.provider];
      }
    }

    const embeddings = db.prepare(`
      SELECT e.chunk_id, e.repo, e.path, e.start_line, e.end_line, e.embedding,
             e.provider, e.model_name, e.dimensions,
             c.title, c.heading_path, c.role, c.visibility, c.public_presence, c.github_url, c.text
      FROM embeddings e
      JOIN chunks c ON e.chunk_id = c.id
      WHERE 1=1 ${publicClause} ${repoClause} ${providerClause}
    `).all(...[...(repo !== "all" ? [repo] : []), ...providerParams]);

    const similarities = embeddings.map(row => {
      const embedding = JSON.parse(row.embedding);
      const similarity = cosineSimilarity(queryEmbedding, embedding);
      const snippet = row.text ? row.text.slice(0, 200).replace(/\n/g, " ") + "..." : "";
      return {
        id: `${row.repo}:${row.path}#L${row.start_line}-L${row.end_line}`,
        repo: row.repo,
        path: row.path,
        title: row.title,
        heading_path: row.heading_path,
        start_line: row.start_line,
        end_line: row.end_line,
        role: row.role,
        visibility: row.visibility,
        score: similarity,
        snippet,
        github_url: row.github_url ? `${row.github_url}#L${row.start_line}-L${row.end_line}` : "",
        _reasons: ["semantic_similarity"],
        _provider: row.provider,
        _model: row.model_name,
        _dimensions: row.dimensions,
      };
    });

    similarities.sort((a, b) => b.score - a.score);
    const results = similarities.slice(0, limit);

    db.close();

    return {
      ok: true,
      query: q,
      mode: "semantic",
      view,
      provider: preferredProvider || providerParams[0] || "unknown",
      count: results.length,
      results: results.map(({ text, _provider, _model, _dimensions, ...r }) => r),
      warnings: [],
    };
  } catch (e) {
    db?.close();
    return { ok: false, error: "semantic_search_failed", query: q, mode: "semantic", ...(view === FULL_VIEW ? { message: e.message } : {}) };
  }
}

async function contextSearch(ctx, query, options = {}) {
  const q = normalizeContextQuery(query);
  const view = normalizeDaemonView(options.view) === FULL_VIEW ? FULL_VIEW : PUBLIC_VIEW;
  const mode = String(options.mode || "keyword").toLowerCase();
  if (!q) return { ok: false, error: "missing_query", query: q };
  if (mode === "semantic") {
    return await semanticSearch(ctx, query, { ...options, view });
  }
  if (!new Set(["keyword", "hybrid"]).has(mode)) {
    return { ok: false, error: "invalid_mode", query: q, mode, available_modes: ["keyword", "hybrid"] };
  }
  const opened = await openIndexDatabase({ create: false });
  if (!opened.ok) return { ok: false, error: opened.error || opened.code, code: opened.code, query: q };
  const { db } = opened;
  try {
    const ftsQuery = ftsQueryFromText(q);
    if (!ftsQuery) return { ok: false, error: "empty_fts_query", query: q };
    const repo = String(options.repo || "all");
    const limit = boundedInteger(options.limit, 10, 1, 50);
    const includeNonPublic = view === FULL_VIEW ? 1 : 0;
    const rows = db.prepare(`
      SELECT
        c.repo, c.path, c.title, c.heading_path, c.start_line, c.end_line,
        c.text, snippet(chunks_fts, -1, '[', ']', '...', 24) AS snippet,
        bm25(chunks_fts) AS raw_rank, c.role, c.visibility, c.public_presence,
        c.github_url, c.searchable_public
      FROM chunks_fts
      JOIN chunks c ON c.id = chunks_fts.chunk_id
      WHERE chunks_fts MATCH ?
        AND (? = 1 OR c.searchable_public = 1)
        AND (? = 'all' OR c.repo = ?)
      ORDER BY raw_rank, c.repo, c.path, c.start_line
      LIMIT ?
    `).all(ftsQuery, includeNonPublic, repo, repo, Math.min(limit * 3, 150));
    const maxRelevance = Math.max(...rows.map(row => Math.abs(Number(row.raw_rank) || 0)), 1e-9);
    const terms = contextQueryTerms(q);
    const ranked = rows.map(row => {
      const base = 0.7 * (Math.abs(Number(row.raw_rank) || 0) / maxRelevance);
      const boosts = contextMetadataBoosts(row, terms, repo, mode);
      return {
        id: contextSourceId(row),
        repo: row.repo,
        path: row.path,
        title: row.title,
        heading_path: row.heading_path,
        start_line: row.start_line,
        end_line: row.end_line,
        role: row.role,
        visibility: row.visibility,
        score: Number(Math.min(1, base + boosts.total).toFixed(6)),
        github_url: row.github_url ? `${row.github_url}#L${row.start_line}-L${row.end_line}` : "",
        snippet: row.snippet,
        ...(options.includeText ? { text: row.text } : {}),
        _raw_rank: Number(row.raw_rank) || 0,
        _reasons: ["matched_terms", ...boosts.reasons],
      };
    }).sort((a, b) => b.score - a.score || a._raw_rank - b._raw_rank || a.id.localeCompare(b.id));
    const results = ranked.slice(0, limit).map(({ _raw_rank, _reasons, ...result }) => result);
    return {
      ok: true,
      query: q,
      mode,
      view,
      count: results.length,
      results,
      warnings: mode === "hybrid" ? ["Hybrid phase 1 uses FTS5 plus deterministic metadata boosts; semantic embeddings are not enabled."] : [],
    };
  } catch (e) {
    return { ok: false, error: "context_search_failed", ...(view === FULL_VIEW ? { message: e.message } : {}), query: q };
  } finally {
    db.close();
  }
}

async function contextPack(ctx, query, options = {}) {
  const q = normalizeContextQuery(query);
  const budget = boundedInteger(options.budget, DEFAULT_CONTEXT_BUDGET, 256, MAX_CONTEXT_BUDGET);
  const view = normalizeDaemonView(options.view) === FULL_VIEW ? FULL_VIEW : PUBLIC_VIEW;
  const search = await contextSearch(ctx, q, { ...options, view, includeText: true });
  if (!search.ok) return search;
  const status = await indexStatus(ctx);
  const context = [];
  const sources = [];
  const warnings = [...search.warnings];
  let used = 0;
  for (const result of search.results) {
    if (used >= budget) break;
    const available = budget - used;
    const originalText = result.text || "";
    const originalEstimate = estimateTokens(originalText);
    let text = originalText;
    let endLine = result.end_line;
    if (originalEstimate > available) {
      text = truncateToTokenBudget(originalText, available);
      if (!text) continue;
      endLine = result.start_line + (text.match(/\n/g) || []).length;
      warnings.push(`Context truncated to budget at ${result.repo}:${result.path}#L${result.start_line}.`);
    }
    const estimate = estimateTokens(text);
    const sourceId = `${result.repo}:${result.path}#L${result.start_line}-L${endLine}`;
    sources.push({
      source_id: sourceId,
      repo: result.repo,
      path: result.path,
      title: result.title,
      heading_path: result.heading_path,
      start_line: result.start_line,
      end_line: endLine,
      role: result.role,
      visibility: result.visibility,
      github_url: result.github_url ? `${result.github_url.replace(/#.*$/, "")}#L${result.start_line}-L${endLine}` : "",
    });
    context.push({ source_id: sourceId, text });
    used += estimate;
  }
  const queryHash = sha256(q);
  const indexHash = status.index_hash || "";
  const logical = {
    query: q,
    repo: options.repo || "all",
    mode: options.mode || "keyword",
    view,
    budget,
    retrieval_policy_version: CONTEXT_RETRIEVAL_POLICY_VERSION,
    index_hash: indexHash,
    sources,
    context,
  };
  return {
    ok: true,
    query: q,
    strategy: CONTEXT_RETRIEVAL_POLICY_VERSION,
    retrieval_policy_version: CONTEXT_RETRIEVAL_POLICY_VERSION,
    view,
    budget: { max_tokens: budget, used_tokens_estimate: used },
    index: { schema_version: status.schema_version || INDEX_SCHEMA_VERSION, index_hash: indexHash },
    query_hash: queryHash,
    index_hash: indexHash,
    pack_hash: sha256(stableJson(logical)),
    sources,
    context,
    instructions_for_model: [
      "Answer from the supplied sources when the question concerns the corpus.",
      "Distinguish facts, hypotheses, interpretations, and items requiring verification.",
      "Report missing context instead of inventing information.",
      "Cite the relevant source_id values.",
    ],
    warnings,
  };
}

