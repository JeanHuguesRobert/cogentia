    const continuations = emitIndexContinuations(ctx, boundaries);
    const boundaryKeys = new Set(boundaries.filter(b => b.blocks_index).map(b => `${b.repo}/${b.path}`));
    const repoByName = new Map(ctx.repos.map(repo => [repo.name, repo]));
